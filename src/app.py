import math

def add (a,b):
    return a + b
    
def subtraction (a,b):
    return a - b

def multiplication (a,b):
    return a * b

def division (a,b):
    return a / b

def square (a):
    return a * a

def log (a):
    return math.log(a)

def sine (a):
    return math.sin(math.radians(a))

def cosine (a):
    return math.cos(math.radians(a))

def square_root (a):
    return math.sqrt(a)

def percentage (a,b):
    return ((division(a,b)) * 100)